export async function generateMetadata({ params }: Props): Promise<Metadata> {
  const { locale } = await params
  const siteUrl = process.env.NEXT_PUBLIC_SITE_URL || 'https://www.hellholewiki.wiki'
  const path = '/about'

  return {
    title: 'About Hellhole Wiki - Your Ultimate Steam Game Resource',
    description: 'Learn about Hellhole Wiki, a community-driven resource hub providing comprehensive guides, crafting tips, creature info, and strategies for the Hellhole Roblox game.',
    robots: {
      index: false,
      follow: true,
      googleBot: {
        index: false,
        follow: true,
        'max-video-preview': -1,
        'max-image-preview': 'large',
        'max-snippet': -1,
      },
    },
    openGraph: {
      type: 'website',
      locale: locale,
      url: locale === 'en' ? `${siteUrl}${path}` : `${siteUrl}/${locale}${path}`,
      siteName: 'Hellhole Wiki',
      title: 'About Hellhole Wiki',
      description: 'Learn about our mission to provide the best Hellhole game resources and guides.',
      images: [
        {
          url: `${siteUrl}/images/hero.webp`,
          width: 1200,
          height: 630,
          alt: 'Hellhole Wiki',
        },
      ],
    },
    twitter: {
      card: 'summary_large_image',
      title: 'About Hellhole Wiki',
      description: 'Learn about our mission to provide the best Hellhole game resources.',
      images: [`${siteUrl}/images/hero.webp`],
    },
    alternates: buildLanguageAlternates(path, locale as Locale, siteUrl),
  }
}

